package conditional_statement;

public class switch_2 {
	public static void main(String[] args) {
		switch("february") {
		case"january":{
			System.out.println("is a first month");
			break;
		}
		case"february":{
			System.out.println("is the second month");
			break;
		}
		case"march":{
			System.out.println("is the third month");
			break;
		}
		case"april":{
			System.out.println("is the fouth month");
			break;
		}
		case"may":{
			System.out.println("is the fifth month");
			break;
		}
		case"june":{
			System.out.println("is the sixth month");
			break;
		}
		case"july":{
			System.out.println("is the seventh month");
			break;
		}
			
		}
	}

}
